#!/bin/bash
# -------------------------------------------------------------------------
# سكريبت الحفظ والبناء (السيرفر - هوستنجر)
# مشروع: تراث مصر - Turath Masr
# -------------------------------------------------------------------------

echo "🚀 جاري فك حزمة التحديث وبناء النسخة الجديدة لمشروع تراث مصر..."

# 1. الدخول لمسار المشروع في السيرفر
cd /www/wwwroot/schools || { echo "❌ خطأ: لم يتم العثور على المسار /www/wwwroot/schools"; exit 1; }

# 2. التأكد من وجود ملف التحديث
if [ ! -f turath_masr_update.zip ]; then
    echo "❌ خطأ: لم يتم العثور على ملف turath_masr_update.zip في السيرفر."
    exit 1
fi

# 3. مسح الملفات القديمة تماماً لضمان نظافة البناء (Clean Build)
echo "🧹 جاري مسح الملفات القديمة والذاكرة المؤقتة..."
rm -rf src public package.json .next node_modules/.cache

# 4. فك ضغط الملف الجديد
echo "📦 جاري فك الحزمة..."
unzip -o turath_masr_update.zip

# 5. تنزيل التحديثات (في حال إضافة مكتبات جديدة)
echo "⬇️ جاري فحص المكتبات (npm install)..."
npm install --no-audit --no-fund

# 6. بناء المشروع (npm run build)
echo "🏗️ جاري بناء المشروع (قد يستغرق دقائق)..."
npm run build

# 7. إعادة تشغيل PM2
echo "🔄 جاري إعادة التشغيل..."
/www/server/nodejs/v22.20.0/bin/pm2 restart turath-masr --update-env

echo "------------------------------------------------------------"
echo "✅ مبروك! الموقع الآن يعمل بأحدث نسخة من مشروع تراث مصر."
echo "------------------------------------------------------------"
